package projekt2;

public class EmployeeDetails {

	private String name;
	private double monthlySlary;
	private int age;
	private String whatever;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getMonthlySlary() {
		return monthlySlary;
	}
	public void setMonthlySlary(double monthlySlary) {
		this.monthlySlary = monthlySlary;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getWhatever() {
		return whatever;
	}
	public void setWhatever(String whatever) {
		this.whatever = whatever;
	}
}
